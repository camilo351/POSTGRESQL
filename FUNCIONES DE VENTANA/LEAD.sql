WITH promedio AS (
     SELECT cl.nombre AS clientes,
	        v.fecha_venta,
	        v.monto,
			COALESCE (LEAD(v.monto, 1)OVER(PARTITION BY cl.nombre ORDER BY v.fecha_venta),0) AS siguiente_monto_1,
			COALESCE (LEAD(v.monto, 2)OVER(PARTITION BY cl.nombre ORDER BY v.fecha_venta),0) AS siguiente_monto_2
	 FROM clientes cl
	 INNER JOIN ventas v
	 ON v.cliente_id = cl.cliente_id
)
SELECT clientes, fecha_venta, monto, siguiente_monto_1, siguiente_monto_2,
      ROUND( AVG(monto + siguiente_monto_1 + siguiente_monto_2), 2) AS media_movil
FROM promedio
GROUP BY clientes, fecha_venta, monto, siguiente_monto_1, siguiente_monto_2;