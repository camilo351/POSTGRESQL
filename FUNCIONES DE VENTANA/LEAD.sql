WITH estado AS (
    SELECT 
	     cl.nombre AS clientes,
		 EXTRACT(MONTH FROM v.fecha_venta) AS mes,
		 EXTRACT(YEAR FROM v.fecha_venta) AS año,
		 v.monto
	FROM clientes cl
	INNER JOIN ventas v
	ON cl.cliente_id = v.cliente_id
)
SELECT clientes, mes, año, monto,
COALESCE (LEAD(monto)OVER(PARTITION BY clientes),0) AS futuro_monto,
CASE 
    WHEN monto > LEAD(monto)OVER(PARTITION BY clientes ORDER BY fecha_venta) THEN 'Bajo'
	WHEN monto < LEAD(monto)OVER(PARTITION BY clientes ORDER BY fecha_venta) THEN 'Subio'
	ELSE 'Igual'
END AS crecimiento
FROM estado;

