WITH siguiente_monto AS (
    SELECT 
	cl.nombre AS clientes,
	pr.nombre AS productos, 
	v.monto
	FROM ventas v
	INNER JOIN productos pr ON pr.producto_id = v.producto_id
	INNER JOIN clientes cl ON cl.cliente_id = v.cliente_id
)
SELECT clientes,productos, monto,
COALESCE (LEAD(monto, 1)OVER(PARTITION BY clientes),0) AS monto_futuro,
CASE 
    WHEN LEAD(monto, 1)OVER(PARTITION BY clientes) - monto IS NULL THEN '0'
	ELSE LEAD(monto, 1)OVER(PARTITION BY clientes) - monto
END AS diferencia
FROM siguiente_monto;

