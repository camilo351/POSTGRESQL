WITH tendencia AS (
    SELECT cl.nombre AS clientes,
	       v.fecha_venta,
		   v.monto,
		   LEAD(v.monto)OVER(PARTITION BY cl.nombre ORDER BY v.fecha_venta) AS siguiente_monto
    FROM clientes cl
	INNER JOIN ventas v
	ON v.cliente_id = cl.cliente_id
)
